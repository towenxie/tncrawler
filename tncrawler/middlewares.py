class ProxyMiddleware(object):
    logger = logging.getLogger('ProxyMiddleware')
    # overwrite process request
    def process_request(self, request, spider):
        proxy_manager = ProxyManager()
        proxy = proxy_manager.get_one()

        if any(proxy) and 'proxy' not in spider.name:
            # Set the location of the proxy
            request.meta['proxy'] = "http://" + proxy['id']
            self.logger.log(logging.INFO, 'The proxy is [%s].' % request.meta['proxy'])

            if hasattr(proxy, 'user') and hasattr(proxy, 'pwd'):
                if proxy['id'].strip() != '' and proxy['pwd'].strip() != '':
                    # Use the following lines if your proxy requires authentication
                    proxy_user_pass = proxy['user'] + ":" + proxy['pwd']
                    # setup basic authentication for the proxy
                    encoded_user_pass = base64.encodestring(proxy_user_pass)
                    request.headers['Proxy-Authorization'] = 'Basic ' + encoded_user_pass

